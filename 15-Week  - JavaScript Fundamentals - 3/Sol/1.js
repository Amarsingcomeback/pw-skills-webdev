// Define car objects
const car = {
  make: "toyota",
  model: "camry",
  year: 2024,
};

// define displayproperties

function displayproperties(displayproperties) {
  console.log(`Car make :${displayproperties.make}`);
  console.log(`Car model :${displayproperties.model}`);
  console.log(`Car year :${displayproperties.year}`);
}

// run the displayproperties funtions
displayproperties(car);
